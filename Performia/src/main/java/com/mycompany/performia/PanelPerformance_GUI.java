    private void updateData(int tugasSelesai, int tugasDP, int totalAssignment,
                            int tugasOnTime, int tugasOverdue, int totalPerformance, int totalXP) {
        pbSelesai.setMaximum(totalAssignment);
        pbSelesai.setValue(tugasSelesai);
        lblAngkaSelesai.setText(tugasSelesai + " / " + totalAssignment);

        pbDalamProgres.setMaximum(totalAssignment);
        pbDalamProgres.setValue(tugasDP);
        lblAngkaDP.setText(tugasDP + " / " + totalAssignment);

        pbOnTime.setMaximum(totalPerformance);
        pbOnTime.setValue(tugasOnTime);
        lblAngkaOT.setText(tugasOnTime + " / " + totalPerformance);

        pbOverdue.setMaximum(totalPerformance);
        pbOverdue.setValue(tugasOverdue);
        lblAngkaOD.setText(tugasOverdue + " / " + totalPerformance);

        lblXP.setText(totalXP + " XP");

        int skorPerforma;
        if(totalPerformance == 0){
            skorPerforma = 0;
        } else {
            skorPerforma = Math.round((tugasOnTime * 100.0f) / totalPerformance);
        }
        
        if (skorPerforma >= 80) {
            pbOnTime.setForeground(new Color(50, 200, 50));
        } else if (skorPerforma >= 60) {
            pbOnTime.setForeground(Color.ORANGE);
        } else {
            pbOnTime.setForeground(new Color(220, 50, 50));
        }

        if (tugasOverdue > 0) {
            pbOverdue.setForeground(new Color(220, 50, 50));
        } else {
            pbOverdue.setForeground(new Color(50, 200, 50));
        }

        this.revalidate();
        this.repaint();
    }
    
