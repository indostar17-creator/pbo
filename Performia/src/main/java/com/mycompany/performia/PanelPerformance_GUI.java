    public PanelPerformance_GUI(Performa performanceData) {
        initComponents();
        if (performanceData != null) {
            previousCompletedAssignments = performanceData.getTugasSelesai();
            updateData(
                    performanceData.getTugasSelesai(),
                    performanceData.getTugasProgres(),
                    performanceData.getTotalTugas(),
                    Math.round(performanceData.getOnTimeRate()),
                    Math.max(performanceData.getTotalTugas() - Math.round(performanceData.getOnTimeRate()), 0),
                    performanceData.getTotalTugas(),
                    performanceData.getTotalXP()
            );
        } else {
            reloadPerformanceData();
        }
    }

    public void refreshPerformanceData() {
        reloadPerformanceData();
    }

    private void reloadPerformanceData() {
        Performia p = new Performia();
        Karyawan k = Performia.listKaryawan.get(akunAktif);
        int totalTugas = k.getTotalTugas();
        int tugasSelesai = 0;
        int tugasOnTime = 0;
        int tugasOverdue = 0;
        LocalDate hariIni = LocalDate.now();

        for (int i = 0; i < totalTugas; i++) {
            Tugas t = k.getListTugas(i);
            boolean selesai = t.getStatus().equalsIgnoreCase("Done");
            boolean overdue = t.getTanggalBatas().isBefore(hariIni);

            if (selesai) {
                tugasSelesai++;
                tugasOnTime++;
            }

            if (overdue) {
                tugasOverdue++;
            }
        }

        int tugasProgres = Math.max(totalTugas - tugasSelesai, 0);
        int totalPerformance = totalTugas;

        if (previousCompletedAssignments >= 0 && tugasSelesai > previousCompletedAssignments && !Performia.listKaryawan.isEmpty()) {
            int newlyCompletedAssignments = tugasSelesai - previousCompletedAssignments;
            int totalBonusXP = 0;

            for (int i = 0; i < newlyCompletedAssignments; i++) {
//                totalBonusXP += generateRandomXpBonus();
//                totalBonusXP += pfm.generateXP();
                Tugas t = k.getListTugas(i);
                int XP = t.getSkorXP();
                
//                totalBonusXP += Performa.getRecentTaskXP();
                totalBonusXP += XP;
            }

//            Performia.listKaryawan.get(0).tambahXP(totalBonusXP);
        }

        previousCompletedAssignments = tugasSelesai;

        int totalXP = 0;

        if (!Performia.listKaryawan.isEmpty()) {
            totalXP = k.getTotalXP();
        }

        updateData(
                tugasSelesai,
                tugasProgres,
                totalTugas,
                tugasOnTime,
                tugasOverdue,
                totalPerformance,
                totalXP
        );
    }

