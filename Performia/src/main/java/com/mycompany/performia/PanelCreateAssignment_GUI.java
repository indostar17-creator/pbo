    public int getIndexKaryawan(){
        String namaInput = textKaryawan.getText().trim();
        for(int i = 0; i < Performia.listKaryawan.size(); i++){
            if(namaInput.equalsIgnoreCase(Performia.listKaryawan.get(i).getNama())){
                return i;
                
            }
        }
        
        return -1;
    }

